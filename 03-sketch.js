// ------------------------------------------------------------------------------------------------------------------------------ 
// Setup() Function
// ------------------------------------------------------------------------------------------------------------------------------ 

function setup() {
  createCanvas(1000, 700);
}


// ------------------------------------------------------------------------------------------------------------------------------ 
// Draw() Function
// ------------------------------------------------------------------------------------------------------------------------------  

function draw() {
  background(210);

// FRAMES -----------------------------------------------------------------------------------------------------------------------
 // Frame for background of screen
  // drawFrame(x1, y1, x2, y2, z)
   // Input:
    // (x1, y1) = top left corner
    // (x2, y2) = bottom right corner
    // z = frame thickness
   drawFrame(0, 0, 1000, 700, 6);

// TABS -----------------------------------------------------------------------------------------------------------------------
 // Colored tab bar at top of screen
  // drawTab( x1, y1, x2, y2, c = "blue")
   // Input:
    // (x1, y1) = top left corner
    // (x2, y2) = bottom right corner
    // c = fill color
   drawTab(10, 10, 990, 40, "#01017A");
  
// TEXT -----------------------------------------------------------------------------------------------------------------------
 // Text on tab bar at top of screen
  // drawText(str, x, y, size, spacing) 
   // Input:
    // (x1, y1) = top left corner
    // (x2, y2) = bottom right corner
    // z = frame thickness  
   drawText("NEW CANVAS", 12, 16, 3, 17);

// BOXES -----------------------------------------------------------------------------------------------------------------------
 // Creates background boxes
  // drawBox(x1, y1, x2, y2, c = "gray") 
   // Input:
    // (x1, y1) = top left corner
    // (x2, y2) = bottom right corner
    // c = fill color  
  
    // Minimize box
   drawBox(200, 200, 300, 300);

  // Maximize box
   drawBox(200, 200, 300, 300);

  // X box
   drawBox(200, 200, 300, 300);
}